/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
	static uint32_t	ui32_BlinkTick = 0;
	static uint32_t	ui32_SelectBlinkTick = 500;	// Startup Tick
	static uint32_t	ui32_KeyPressTick = 0;	// KeyPress Tick
	static uint32_t	ui32_USBPrintfTick = 0;
	char HiMsg[DEF_MAX_MSG_LENGTH] = "SMT32G431CB Schwartz pill USB DEMO    \r\n";
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_UCPD1_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_LPUART1_UART_Init();
  MX_RTC_Init();
  MX_SPI3_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  MX_USB_Device_Init();
  /* USER CODE BEGIN 2 */
  // Forca a reenumeracao do USB
  LL_GPIO_SetOutputPin(PC13_LED0_GPIO_Port, PC13_LED0_Pin);
  LL_GPIO_SetOutputPin(GPIOA, LL_GPIO_PIN_15);
  HAL_Delay(500);
  LL_GPIO_ResetOutputPin(GPIOA, LL_GPIO_PIN_15);
  LL_GPIO_ResetOutputPin(PC13_LED0_GPIO_Port, PC13_LED0_Pin);
  HAL_Delay(5000);
  //******************************
  bootinfo();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  uint32_t u32_ActualTick = HAL_GetTick();
	  if (u32_ActualTick-ui32_BlinkTick > ui32_SelectBlinkTick)
	  {
		  ui32_BlinkTick = u32_ActualTick;
		  LL_GPIO_TogglePin(PC13_LED0_GPIO_Port, PC13_LED0_Pin);
	  }

	  if (!LL_GPIO_IsInputPinSet(PC10_KEY0_GPIO_Port, PC10_KEY0_Pin))
	  {
		  if (u32_ActualTick - ui32_KeyPressTick > 10){
			// Valid KeyPress
			  ui32_KeyPressTick = u32_ActualTick;
			  ui32_SelectBlinkTick++;
			  if (ui32_SelectBlinkTick > 1000)
				  ui32_SelectBlinkTick = 50;
		  }
	  } else
	  {
		  ui32_KeyPressTick = u32_ActualTick;
	  }


	  if (u32_ActualTick-ui32_USBPrintfTick > 200)
	  {
		  ui32_USBPrintfTick = u32_ActualTick;
		  snprintf(HiMsg, DEF_MAX_MSG_LENGTH-1, "Actual Tick: %lu, Blink Tick: %lu\r\n",
				  u32_ActualTick, ui32_SelectBlinkTick);
	  	  CDC_Transmit_FS(HiMsg, strlen(HiMsg));
	  }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

